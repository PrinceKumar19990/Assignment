 
class IfFbd {
    public static void main(String args[])
    {
        String str = "FbdFORFbd";
        int i = 8;
 
        // if block
        if (i == 8) {
            i++;
            System.out.println(str);
        }
 
        // Executed by default
        System.out.println("i = " + i);
    }
}